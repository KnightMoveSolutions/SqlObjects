namespace KnightMoves.SqlObjects.SqlCode
{
    /// <summary>
    /// Used to identify the SQL arithmetic operations.
    /// </summary>
    public enum SqlArithmeticOperators
    {
        /// <summary>
        /// Corresponds to the '+' operator in SQL
        /// </summary>
        Plus,

        /// <summary>
        /// Corresponds to the '-' operator in SQL
        /// </summary>
        Minus,

        /// <summary>
        /// Corresponds to the '*' operator in SQL
        /// </summary>
        Multiply,

        /// <summary>
        /// Corresponds to the '/' operator in SQL
        /// </summary>
        Divide,

        /// <summary>
        /// Corresponds to the '%' operator in SQL
        /// </summary>
        Modulo
    }
}
